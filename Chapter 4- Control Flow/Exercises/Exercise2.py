# This example is what happens when the 'if' statement has met the value.
alien_color = "yellow"
# If they right value is matched it will print "5 Points!"
if alien_color == "yellow" :
   print ("5 Points!")
   # If it is not met it will print "10 Points! instead"
else: print ("10 Points!")

# This example is what happens when the 'if' statements value is not met.
alien_color = "Red"
# The value is not gonna be met so 'if' statement will not activate
if alien_color == "yellow" :
   print ("5 Points!")
   # Since the value doesn't fufill the 'if' statement the 'else' statement will activate and print "10 Points!"
else: print ("10 Points!")

